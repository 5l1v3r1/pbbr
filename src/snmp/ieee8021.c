int
sa_start_timer(long duration)
{
	if (sa_alarms) {
		struct itimerval setting = {
			{0, 0},
			{duration / 1000, (duration % 1000) * 1000}
		};
		if (sa_alm_catch())
			return (-1);
		if (setitimer(ITIMER_REAL, &setting, NULL))
			return (-1);
		sa_alm_signal = 0;
		return (0);
	} else {
#if defined NETSNMP_DS_APPLICATION_ID
		struct timeval setting = {
			duration / 1000, (duration % 1000) * 1000
		};
		sa_alm_handle = snmp_alarm_register_hr(setting, 0, sa_snmp_alm_handler, NULL);
#else
		sa_alm_handle = snmp_alarm_register((duration + 999) / 1000, 0, sa_snmp_alm_handler, NULL);
#endif
		return (sa_alm_handle ? 0 : -1);
	}
}

static void
sa_exit(int retval)
{
	if (retval)
		snmp_log(MY_FACILITY(LOG_ERR), "%s: Exiting %d", sa_program, retval);
	else
		snmp_log(MY_FACILITY(LOG_NOTICE), "%s: Exiting %d", sa_program, retval);
	fflush(stdout);
	fflush(stderr);
	sa_sig_block();
	closelog();
	exit(retval);
}

static void
sa_init_logging(int argc, char *argv[])
{
	static char progname[256];

	/* The purpose of this function is to bring logging up before forking (and while still in the foreground) so that we can use the snmp_log() function before and during forking if necessary.
	   Note that the default configuration for snmp_log() is to send all logs to standard error. */
	strncpy(progname, basename(argv[0]), sizeof(progname));
	snmp_disable_log();
	if (sa_logfillog) {
		snmp_enable_filelog(sa_logfile, sa_appendlog);
	}
	if (sa_logstderr || sa_logstdout) {
#if defined LOG_PERROR
		/* Note that when we have Linux LOG_PERROR, and logs go both to syslog and stderr, it is better to use the LOG_PERROR than to use snmp_log()'s print to stderr, as the former is better 
		   formated. */
		if (!sa_logsyslog)
			snmp_enable_stderrlog();
#else				/* defined LOG_PERROR */
		snmp_enable_stderrlog();
#endif				/* defined LOG_PERROR */
	}
	if (sa_logsyslog) {
#if !defined HAVE_SNMP_ENABLE_SYSLOG_IDENT
		snmp_enable_syslog();
#else				/* !defined HAVE_SNMP_ENABLE_SYSLOG_IDENT */
		snmp_enable_syslog_ident("ieee8021", LOG_DAEMON);
#endif				/* !defined HAVE_SNMP_ENABLE_SYSLOG_IDENT */
		/* Note that the way that snmp sets up the logger is not really the way we want it, so close the log and reopen it the way we want. */
		closelog();
#if defined LOG_PERROR
		openlog("ieee8021", LOG_PID | LOG_CONS | LOG_NDELAY | (sa_logstderr ? LOG_PERROR : 0), MY_FACILITY(0));
#else				/* defined LOG_PERROR */
		openlog("ieee8021", LOG_PID | LOG_CONS | LOG_NDELAY, MY_FACILITY(0));
#endif				/* defined LOG_PERROR */
	}
	if (sa_logcallog) {
		snmp_enable_calllog();
	}
}

static void
sa_enter(int argc, char *argv[])
{
	if (sa_nomead) {
		pid_t pid;

		if ((pid = fork()) < 0) {
			perror(argv[0]);
			exit(2);
		} else if (pid != 0) {
			/* parent exits */
			exit(0);
		}
		setsid();	/* become a session leader */
		/* fork once more for SVR4 */
		if ((pid = fork()) < 0) {
			perror(argv[0]);
			exit(2);
		} else if (pid != 0) {
			/* parent responsible for writing pid file */
			if (sa_nomead || sa_pidfile[0] != '\0') {
				FILE *pidf;

				/* initialize default filename */
				if (sa_pidfile[0] == '\0')
					snprintf(sa_pidfile, sizeof(sa_pidfile), "/var/run/%s.pid", sa_program);
				if (sa_output > 1) {
					snmp_log(MY_FACILITY(LOG_NOTICE), "%s: Writing daemon pid to file %s", sa_program, sa_pidfile);
				}
				if ((pidf = fopen(sa_pidfile, "w+"))) {
					fprintf(pidf, "%d", (int) pid);
					fflush(pidf);
					fclose(pidf);
				} else {
					snmp_log(MY_FACILITY(LOG_ERR), "%s: %m", sa_program);
					snmp_log(MY_FACILITY(LOG_ERR), "%s: Could not write pid to file %s", sa_program, sa_pidfile);
					sa_exit(2);
					/* no return */
				}
			}
			/* parent exits */
			exit(0);
		}
		/* child continues */
		/* release current directory */
		if (chdir("/") < 0) {
			perror(argv[0]);
			exit(2);
		}
		umask(0);	/* clear file creation mask */
		/* rearrange file streams */
		fclose(stdin);
	}
	/* continue as foreground or background */
	sa_init_logging(argc, argv);
	sa_sig_catch();
	if (sa_agentx) {
		if (sa_debug)
			snmp_log(MY_FACILITY(LOG_DEBUG), "%s: running as AgentX client\n", argv[0]);
		/* run as an AgentX client */
		ds_set_boolean(DS_APPLICATION_ID, DS_AGENT_ROLE, 1);
	} else {
		if (sa_debug)
			snmp_log(MY_FACILITY(LOG_DEBUG), "%s: running as SNMP master agent\n", argv[0]);
		/* run as SNMP master */
		ds_set_boolean(DS_APPLICATION_ID, DS_AGENT_ROLE, 0);
	}
	if (sa_alarms) {
		if (sa_debug)
			snmp_log(MY_FACILITY(LOG_DEBUG), "%s: using application alarms\n", argv[0]);
		/* use application alarms */
		ds_set_boolean(DS_LIBRARY_ID, DS_LIB_ALARM_DONT_USE_SIG, 1);
	}
	/* initialize agent */
	init_agent("ieee8021");
	/* initialize MIB */
	init_ieee8021TcMib();
	/* initialize SNMP */
	init_snmp("ieee8021");
	if (!sa_agentx) {
		if (sa_debug)
			snmp_log(MY_FACILITY(LOG_DEBUG), "%s: running as SNMP master\n", argv[0]);
#if !defined NETSNMP_DS_APPLICATION_ID
		init_master_agent(710, NULL, NULL);
#else
		init_master_agent();
#endif
	}
	snmp_log(MY_FACILITY(LOG_NOTICE), "%s: Startup complete.", sa_program);
}

static int
sa_check_and_process(void)
{
	int numfds;
	fd_set readfds, writefds, exceptfds;
	struct timeval timeout, *tvp;
	int count, block, i;

	/* check signals */
	if (sa_alm_signal)
		sa_alm_action();
	if (sa_pol_signal)
		sa_pol_action();
	if (sa_hup_signal)
		sa_hup_action();
	if (sa_int_signal)
		sa_int_action();	/* no return */
	if (sa_trm_signal)
		sa_trm_action();	/* no return */

	for (i = 0; i < NUM_EXTERNAL_SIGS; i++) {
		if (external_signal_scheduled[i]) {
			external_signal_scheduled[i]--;
			external_signal_handler[i] (i);
		}
	}

	numfds = 0;
	FD_ZERO(&readfds);
	FD_ZERO(&writefds);
	FD_ZERO(&exceptfds);
	timeout.tv_sec = 0;
	timeout.tv_usec = 500000UL;
	block = 0;
	snmp_select_info(&numfds, &readfds, &timeout, &block);
	tvp = (block == 1) ? NULL : &timeout;

	for (i = 0; i < external_readfdlen; i++) {
		FD_SET(external_readfd[i], &readfds);
		if (external_readfd[i] >= numfds)
			numfds = external_readfd[i] + 1;
	}
	for (i = 0; i < external_writefdlen; i++) {
		FD_SET(external_writefd[i], &writefds);
		if (external_writefd[i] >= numfds)
			numfds = external_writefd[i] + 1;
	}
	for (i = 0; i < external_exceptfdlen; i++) {
		FD_SET(external_exceptfd[i], &exceptfds);
		if (external_exceptfd[i] >= numfds)
			numfds = external_exceptfd[i] + 1;
	}

	count = select(numfds, &readfds, &writefds, &exceptfds, tvp);

	if (count > 0) {

		snmp_read(&readfds);

		for (i = 0; count && (i < external_readfdlen); i++) {
			if (FD_ISSET(external_readfd[i], &readfds)) {
				external_readfdfunc[i] (external_readfd[i], external_readfd_data[i]);
				FD_CLR(external_readfd[i], &readfds);
				count--;
			}
		}
		for (i = 0; count && (i < external_writefdlen); i++) {
			if (FD_ISSET(external_writefd[i], &writefds)) {
				external_writefdfunc[i] (external_writefd[i], external_writefd_data[i]);
				FD_CLR(external_writefd[i], &writefds);
				count--;
			}
		}
		for (i = 0; count && (i < external_exceptfdlen); i++) {
			if (FD_ISSET(external_exceptfd[i], &exceptfds)) {
				external_exceptfdfunc[i] (external_exceptfd[i], external_exceptfd_data[i]);
				FD_CLR(external_exceptfd[i], &exceptfds);
				count--;
			}
		}

	} else {
		switch (count) {
		case 0:
			snmp_timeout();
			break;
		case -1:
			if (errno != EINTR)
				snmp_log_perror("select");
			return (count);
		default:
			snmp_log(LOG_ERR, "select returned %d\n", count);
			return (count);
		}		/* endif -- count>0 */
	}

	run_alarms();
	return (count);
}

static void
sa_mloop(int argc, char *argv[])
{
	int retval;

	for (;;) {

		if ((retval = sa_check_and_process()) <= 0) {
			/* did not process packets or read fds */
			switch (retval) {
			case 0:
				/* timeout */
				continue;
			case -1:
				/* error */
				switch (errno) {
				case EINTR:
				case EAGAIN:
				case ETIMEDOUT:
					continue;
				}
			default:
				/* fatal error */
				break;
			}
			break;
		}
	}
	if (sa_debug)
		snmp_log(MY_FACILITY(LOG_DEBUG), "%s: shutting down\n", argv[0]);
	snmp_shutdown("ieee8021");
}


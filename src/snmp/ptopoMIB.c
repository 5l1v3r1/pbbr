/**
 * @fn u_char * var_ptopoMIB(struct variable *vp, oid *name, size_t *length, int exact, size_t *var_len, WriteMethod **write_method)
 * @param vp a pointer to the entry in the variables table for the requested variable.
 * @param name the object identifier for which to find.
 * @param length the length of the object identifier.
 * @param exact whether the name is exact.
 * @param var_len a pointer to the length of the representation of the object.
 * @param write_method a pointer to a write method for the object.
 * @brief locate variables in ptopoMIB.
 *
 * This function returns a pointer to a memory area that is static across the request that contains
 * the UCD-SNMP representation of the scalar (so that it may be used to read from for a GET,
 * GET-NEXT or GET-BULK request).  This returned pointer may be NULL, in which case the function is
 * telling UCD-SNMP that the scalar does not exist for reading; however, if write_method is
 * overwritten with a non-NULL value, the function is telling UCD-SNMP that the scalar exists for
 * writing.  Write-only objects can be effected in this way.
 */
u_char *
var_ptopoMIB(struct variable *vp, oid * name, size_t *length, int exact, size_t *var_len, WriteMethod ** write_method)
{
	struct ptopoMIB_data *StorageTmp;
	u_char *rval;

	DEBUGMSGTL(("ptopoMIB", "var_ptopoMIB: lookup up varbind...  "));
	if (header_generic(vp, name, length, exact, var_len, write_method) == MATCH_FAILED)
		return NULL;
	/* Refresh the MIB values if required. */
	refresh_ptopoMIB(0);
	if ((StorageTmp = ptopoMIBStorage) == NULL) {
		DEBUGMSGTL(("ptopoMIB", "no datastructure.\n"));
		return NULL;
	}
	*write_method = NULL;
	*var_len = 0;
	rval = NULL;
	/* This is where we do the value assignments for the mib results. */
	switch (vp->magic) {
	case (u_char) PTOPOLASTCHANGETIME:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoLastChangeTime);
		rval = (u_char *) &StorageTmp->ptopoLastChangeTime;
		break;
	case (u_char) PTOPOCONNTABINSERTS:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConnTabInserts);
		rval = (u_char *) &StorageTmp->ptopoConnTabInserts;
		break;
	case (u_char) PTOPOCONNTABDELETES:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConnTabDeletes);
		rval = (u_char *) &StorageTmp->ptopoConnTabDeletes;
		break;
	case (u_char) PTOPOCONNTABDROPS:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConnTabDrops);
		rval = (u_char *) &StorageTmp->ptopoConnTabDrops;
		break;
	case (u_char) PTOPOCONNTABAGEOUTS:	/* ReadOnly */
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConnTabAgeouts);
		rval = (u_char *) &StorageTmp->ptopoConnTabAgeouts;
		break;
	case (u_char) PTOPOCONFIGTRAPINTERVAL:	/* ReadWrite */
		*write_method = write_ptopoConfigTrapInterval;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConfigTrapInterval);
		rval = (u_char *) &StorageTmp->ptopoConfigTrapInterval;
		break;
	case (u_char) PTOPOCONFIGMAXHOLDTIME:	/* ReadWrite */
		*write_method = write_ptopoConfigMaxHoldTime;
		if (!StorageTmp)
			break;
		*var_len = sizeof(StorageTmp->ptopoConfigMaxHoldTime);
		rval = (u_char *) &StorageTmp->ptopoConfigMaxHoldTime;
		break;
	default:
		ERROR_MSG("");
	}
	if (rval)
		DEBUGMSGTL(("ptopoMIB", "found.\n"));
	else
		DEBUGMSGTL(("ptopoMIB", "not found.\n"));
	return (rval);
}


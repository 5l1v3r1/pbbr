{
	DEBUGMSGTL(("lldpXdot3MIB", "lldpXdot3MIB_loop_handler: executing loop handler...  "));
	/* close files after each request */
	if (sa_fclose) {
		if (my_fd >= 0) {
			close(my_fd);
			my_fd = -1;
		}
	}
#if defined MASTER
	/* prepare for next request */
	sa_request++;
#endif				/* defined MASTER */
	if (external_signal_scheduled[sig] == 0)
		external_signal_scheduled[sig]--;
	if (lldpXdot3MIBold_signal_handler != NULL)
		(*lldpXdot3MIBold_signal_handler) (sig);
	DEBUGMSGTL(("lldpXdot3MIB", "done.\n"));
}

